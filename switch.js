var jogador1 = 0;
var jogador2 = 1;
var placar; 

// usando switch case
switch (placar) {
    case placar = jogador1 > jogador2:
        console.log('jogador 1 ganhou');
        break;
    case placar = jogador2 > jogador1:
        console.log('jogador 2 ganhou');
        break;
    default:
        console.log('ninguem marcou ponto')
} 